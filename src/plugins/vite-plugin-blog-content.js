import fs from "fs";
import path from "path";
import matter from "gray-matter";

/**
 * Vite plugin: scans content/posts/*.md at build time,
 * parses frontmatter + content, matches .zh.md translations,
 * and generates src/generated/posts-data.js
 *
 * Also scans content/notes/*.md for short notes/jottings,
 * generates src/generated/notes-data.js with category + tags support.
 */
export function blogContentPlugin() {
  const postsDir = path.resolve(process.cwd(), "content/posts");
  const notesDir = path.resolve(process.cwd(), "content/notes");
  const postsOutput = path.resolve(process.cwd(), "src/generated/posts-data.js");
  const notesOutput = path.resolve(process.cwd(), "src/generated/notes-data.js");
  const statsOutput = path.resolve(process.cwd(), "src/generated/site-stats.js");

  // ── Posts ──────────────────────────────────────────────

  function generatePostsData() {
    if (!fs.existsSync(postsDir)) {
      console.log("[blog-content] No content/posts directory found, generating empty data.");
      const empty = `// Auto-generated by vite-plugin-blog-content.js — DO NOT EDIT\nexport const posts = [];\n`;
      ensureDir(path.dirname(postsOutput));
      fs.writeFileSync(postsOutput, empty, "utf-8");
      return;
    }

    const files = fs.readdirSync(postsDir);
    const enFiles = files.filter((f) => f.endsWith(".md") && !f.endsWith(".zh.md"));
    const zhFiles = files.filter((f) => f.endsWith(".zh.md"));

    const posts = enFiles.map((file) => {
      const filePath = path.join(postsDir, file);
      const raw = fs.readFileSync(filePath, "utf-8");
      const { data: fm, content } = matter(raw);

      const baseName = file.replace(/\.md$/, "");
      const zhFileName = `${baseName}.zh.md`;
      let contentZh = "";
      const zhExists = zhFiles.includes(zhFileName);
      if (zhExists) {
        const zhRaw = fs.readFileSync(path.join(postsDir, zhFileName), "utf-8");
        const { content: zhBody } = matter(zhRaw);
        contentZh = zhBody;
      }

      return {
        slug: fm.slug || baseName,
        title: fm.title || baseName,
        titleZh: fm.titleZh || "",
        excerpt: fm.excerpt || "",
        excerptZh: fm.excerptZh || "",
        category: fm.category || "Thoughts",
        date: fm.date || "",
        readTime: fm.readTime || "5 min",
        coverImage:
          fm.coverImage ||
          `https://picsum.photos/seed/${encodeURIComponent(
            fm.slug || fm.category || "blog"
          )}/800/400`,
        featured: fm.featured || false,
        content,
        contentZh,
      };
    });

    // sort by date desc
    posts.sort((a, b) => new Date(b.date) - new Date(a.date));

    const output = `// Auto-generated by vite-plugin-blog-content.js — DO NOT EDIT
export const posts = ${JSON.stringify(posts, null, 2)};
`;

    ensureDir(path.dirname(postsOutput));
    fs.writeFileSync(postsOutput, output, "utf-8");
    console.log(`[blog-content] Generated ${posts.length} posts → ${path.relative(process.cwd(), postsOutput)}`);
  }

  // ── Notes ──────────────────────────────────────────────

  function generateNotesData() {
    if (!fs.existsSync(notesDir)) {
      console.log("[blog-content] No content/notes directory found, generating empty data.");
      const empty = `// Auto-generated by vite-plugin-blog-content.js — DO NOT EDIT\nexport const notes = [];\nexport const noteCategories = [];\n`;
      ensureDir(path.dirname(notesOutput));
      fs.writeFileSync(notesOutput, empty, "utf-8");
      return;
    }

    const files = fs.readdirSync(notesDir);
    const enFiles = files.filter((f) => f.endsWith(".md") && !f.endsWith(".zh.md"));
    const zhFiles = files.filter((f) => f.endsWith(".zh.md"));

    const notes = enFiles.map((file) => {
      const filePath = path.join(notesDir, file);
      const raw = fs.readFileSync(filePath, "utf-8");
      const { data: fm, content } = matter(raw);

      const baseName = file.replace(/\.md$/, "");
      const zhFileName = `${baseName}.zh.md`;
      let contentZh = "";
      const zhExists = zhFiles.includes(zhFileName);
      if (zhExists) {
        const zhRaw = fs.readFileSync(path.join(notesDir, zhFileName), "utf-8");
        const { content: zhBody } = matter(zhRaw);
        contentZh = zhBody;
      }

      return {
        slug: fm.slug || baseName,
        title: fm.title || baseName,
        titleZh: fm.titleZh || "",
        category: fm.category || "Other",
        date: fm.date || "",
        readTime: fm.readTime || "2 min",
        tags: Array.isArray(fm.tags) ? fm.tags : [],
        content,
        contentZh,
      };
    });

    // sort by date desc
    notes.sort((a, b) => new Date(b.date) - new Date(a.date));

    // extract unique categories in order of first appearance
    const seen = new Set();
    const noteCategories = [];
    for (const n of notes) {
      if (!seen.has(n.category)) {
        seen.add(n.category);
        noteCategories.push(n.category);
      }
    }

    const output = `// Auto-generated by vite-plugin-blog-content.js — DO NOT EDIT
export const notes = ${JSON.stringify(notes, null, 2)};
export const noteCategories = ${JSON.stringify(noteCategories)};
`;

    ensureDir(path.dirname(notesOutput));
    fs.writeFileSync(notesOutput, output, "utf-8");
    console.log(`[blog-content] Generated ${notes.length} notes → ${path.relative(process.cwd(), notesOutput)}`);
  }

  // ── Site Stats ────────────────────────────────────────

  function countWords(text) {
    if (!text) return 0;
    // Remove markdown syntax, code blocks, HTML tags
    const cleaned = text
      .replace(/```[\s\S]*?```/g, "") // code blocks
      .replace(/`[^`]*`/g, "") // inline code
      .replace(/<[^>]+>/g, "") // HTML tags
      .replace(/[#>*_\-|]/g, " ") // markdown syntax chars
      .replace(/\s+/g, " ")
      .trim();
    if (!cleaned) return 0;
    // Count CJK characters + word segments
    const cjk = (cleaned.match(/[\u4e00-\u9fff\u3400-\u4dbf]/g) || []).length;
 const words = (cleaned.replace(/[\u4e00-\u9fff\u3400-\u4dbf]/g, " ").match(/\S+/g) || []).length;
    return cjk + words;
  }

  function generateSiteStats() {
    let totalPosts = 0;
    let totalNotes = 0;
    let totalWords = 0;
    let latestDate = "";

    // Count from posts data file
    if (fs.existsSync(postsOutput)) {
      const postsRaw = fs.readFileSync(postsOutput, "utf-8");
      try {
        const postsMatch = postsRaw.match(/export const posts = ([\s\S]*?);$/m);
        if (postsMatch) {
          const posts = JSON.parse(postsMatch[1]);
          totalPosts = posts.length;
          for (const p of posts) {
            totalWords += countWords(p.content);
            if (p.date && p.date > latestDate) latestDate = p.date;
          }
        }
      } catch {}
    }

    // Count from notes data file
    if (fs.existsSync(notesOutput)) {
      const notesRaw = fs.readFileSync(notesOutput, "utf-8");
      try {
        const notesMatch = notesRaw.match(/export const notes = ([\s\S]*?);$/m);
        if (notesMatch) {
          const notes = JSON.parse(notesMatch[1]);
          totalNotes = notes.length;
          for (const n of notes) {
            totalWords += countWords(n.content);
            if (n.date && n.date > latestDate) latestDate = n.date;
          }
        }
      } catch {}
    }

    const stats = { totalPosts, totalNotes, totalWords, latestDate };

    const output = `// Auto-generated by vite-plugin-blog-content.js — DO NOT EDIT
export const siteStats = ${JSON.stringify(stats, null, 2)};
`;

    ensureDir(path.dirname(statsOutput));
    fs.writeFileSync(statsOutput, output, "utf-8");
    console.log(`[blog-content] Generated site stats → ${path.relative(process.cwd(), statsOutput)}`);
  }

  // ── Shared ─────────────────────────────────────────────

  function ensureDir(dir) {
    if (!fs.existsSync(dir)) {
      fs.mkdirSync(dir, { recursive: true });
    }
  }

  function generateAll() {
    generatePostsData();
    generateNotesData();
    generateSiteStats();
  }

  // Combine directories to watch
  const allWatchDirs = [
    { dir: postsDir, label: "posts" },
    { dir: notesDir, label: "notes" },
  ].filter((d) => fs.existsSync(d.dir));

  return {
    name: "blog-content",
    buildStart() {
      generateAll();
    },
    configureServer(server) {
      // generate on start
      generateAll();

      // watch for changes in each directory
      const watchers = allWatchDirs.map(({ dir, label }) => {
        const w = fs.watch(dir, { recursive: true }, (eventType, filename) => {
          if (filename && (filename.endsWith(".md") || filename.endsWith(".zh.md"))) {
            console.log(`[blog-content] Change detected in ${label}: ${filename}, regenerating...`);
            generateAll();
            // trigger HMR
            server.ws.send({ type: "full-reload" });
          }
        });
        return w;
      });

      server.httpServer.on("close", () => {
        watchers.forEach((w) => w.close());
      });
    },
  };
}
